    private void btnSalidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalidaActionPerformed
        try {
            Statement comando = conexion.createStatement();
            int fila_seleccionada = tblInspeccion.getSelectedRow();
            if (fila_seleccionada >= 0) {
                String componente = (String) tblInspeccion.getValueAt(fila_seleccionada, 0);
                ResultSet registroP = comando.executeQuery("SELECT * FROM almaceninspeccion WHERE componente= '" + componente + "' AND existencia!=''");

                if (registroP.next() == true) {
                    InspeccionGUI.this.dispose();
                    Salida modificar = new Salida(this.ins.get(fila_seleccionada), mod);
                    modificar.setVisible(true);
                    modificar.setLocationRelativeTo(null);
                } else {
                    JOptionPane.showMessageDialog(this, "NO HAY EXISTENCIA EN ALMACEN.");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Por favor seleccione una fila.");
            }
        } catch (SQLException ex) {
            Logger.getLogger(InspeccionGUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(InspeccionGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSalidaActionPerformed

