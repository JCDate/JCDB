    private void tblInspeccionKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblInspeccionKeyReleased
        if (evt.getKeyCode() == com.sun.glass.events.KeyEvent.VK_ENTER) {
            try {
                String componente = tblInspeccion.getValueAt(tblInspeccion.getSelectedRow(), 0).toString();
                String cr = tblInspeccion.getValueAt(tblInspeccion.getSelectedRow(), 1).toString();
                PreparedStatement consulta;
                consulta = conexion.prepareStatement("UPDATE crs SET cr='" + cr + "' WHERE componente='" + componente + "'");
                consulta.executeUpdate();
                JOptionPane.showMessageDialog(this, "MODIFICACIÓN EXITOSA");
            } catch (SQLException ex) {
                Logger.getLogger(InspeccionGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_tblInspeccionKeyReleased

