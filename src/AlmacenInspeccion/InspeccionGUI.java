    private void INS() {
        try {
            this.ins = this.ins_servicio.recuperarTodas(conexion);

            DefaultTableModel dtm = (DefaultTableModel) tblInspeccion.getModel();
            dtm.setRowCount(0);
            for (int i = 0; i < this.ins.size(); i++) {
                dtm.addRow(new Object[]{
                    this.ins.get(i).getComponente(),
                    this.ins.get(i).getExistencia(),
                    this.ins.get(i).getEntrada(),
                    this.ins.get(i).getFechaEntrada(),
                    this.ins.get(i).getSalida()
                });
            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se han podido recuperar los registros");
        }
    }

